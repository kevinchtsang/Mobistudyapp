export default `Mobistudy is an open iniative that allows citizens to contribute \
to clinical research with their data. Participants download a smartphone app \
and, after registering and filling-in a short profile, they are proposed with \
research studies. If they find a study they are interested in, they are \
guided through an easy-to-understand informed consent process. Upon acceptance, \
participants are then proposed tasks, such as answering to questionnaires, \
performing simple exercises or providing data collected by mobile phones, \
wearable devices, or medical sensors.<br><br>
The data collected in this way is kept safe within the Mobistudy servers \
and is shared only with the researchers who proposed the study.`
